import Brick from '../Objects/Brick';

/**
 * Generate the level.
 *
 * @param game
 * @param level
 *
 * @returns {*[]}
 */
export function buildLevel(game, level) {
    let bricks = [];

    level.forEach((row, rowIndex) => {
        row.forEach((brick, brickIndex) => {
            let isSolid = false;
            let position = {
                x: 38 * brickIndex + 57,
                y: 29 + 24 * rowIndex,
            };
            if (brick === 1) {
                bricks.push(new Brick(game, position, isSolid));
            }
            if (brick === 2) {
                isSolid = true;
                bricks.push(new Brick(game, position, isSolid));
            }
        });
    });

    return bricks;
}

